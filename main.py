
import asyncio, os
from videosdk.agents import Agent, AgentSession, CascadingPipeline, JobContext, RoomOptions, WorkerJob,ConversationFlow
from videosdk.plugins.silero import SileroVAD
from videosdk.plugins.turn_detector import TurnDetector, pre_download_model
from videosdk.plugins.deepgram import DeepgramSTT
from videosdk.plugins.openai import OpenAILLM
from videosdk.plugins.elevenlabs import ElevenLabsTTS
from typing import AsyncIterator

# Pre-downloading the Turn Detector model
pre_download_model()

class MyVoiceAgent(Agent):
    def __init__(self):
        super().__init__(instructions="""You are a knowledgeable and compassionate healthcare AI agent, dedicated to supporting patients, caregivers, and healthcare professionals with accurate, up-to-date medical information and guidance. Your primary role is to answer questions related to symptoms, conditions, medications, preventive care, wellness strategies, and healthcare processes. You should provide clear explanations, help users understand medical terminology, and guide them in navigating healthcare systems, scheduling appointments, or accessing resources. Always maintain an empathetic, respectful, and reassuring tone, prioritizing privacy and sensitivity. You are not a substitute for professional medical advice, diagnosis, or treatment—remind users to consult licensed healthcare providers for urgent or personal medical concerns. Avoid making diagnoses or prescribing medications. Communicate clearly, adapt explanations to users’ understanding, and support informed decision-making while upholding confidentiality and trust.""")
    async def on_enter(self): await self.session.say("Hello! How can I help you today regarding healthcare ai agent?")
    async def on_exit(self): await self.session.say("Goodbye!")

async def start_session(context: JobContext):
    # Create agent and conversation flow
    agent = MyVoiceAgent()
    conversation_flow = ConversationFlow(agent)

    # Create pipeline
    pipeline = CascadingPipeline(
        stt=DeepgramSTT(model="nova-2", language="en"),
        llm=OpenAILLM(model="gpt-4o"),
        tts=ElevenLabsTTS(model="eleven_flash_v2_5"),
        vad=SileroVAD(threshold=0.35),
        turn_detector=TurnDetector(threshold=0.8)
    )

    session = AgentSession(
        agent=agent,
        pipeline=pipeline,
        conversation_flow=conversation_flow
    )

    try:
        await context.connect()
        await session.start()
        # Keep the session running until manually terminated
        await asyncio.Event().wait()
    finally:
        # Clean up resources when done
        await session.close()
        await context.shutdown()

def make_context() -> JobContext:
    room_options = RoomOptions(
     #  room_id="YOUR_MEETING_ID",  # Set to join a pre-created room; omit to auto-create
        name="VideoSDK Cascaded Agent for healthcare ai agent",
        playground=True
    )

    return JobContext(room_options=room_options)

if __name__ == "__main__":
    job = WorkerJob(entrypoint=start_session, jobctx=make_context)
    job.start()
